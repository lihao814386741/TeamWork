class Solution {
public:
    bool searchMatrix(vector<vector<int>>& matrix, int target) {
        if (matrix.empty()) {
            return false;
        }
        
        int row = 0;
        int column = matrix.at(0).size() - 1;
        while (row < matrix.size() && column >= 0) {
            if (target == matrix.at(row).at(column)) {
                return true;
            } else if (target < matrix.at(row).at(column)) {
                --column;
            } else {
                ++row;
            }
        }
        return false;
    }
};// My solution can only be O(m + n);
